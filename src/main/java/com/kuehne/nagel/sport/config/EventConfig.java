package com.kuehne.nagel.sport.config;

public class EventConfig {

  double a;
  double b;
  double c;

  public EventConfig(double a, double b, double c) {
    this.a = a;
    this.b = b;
    this.c = c;
  }

  public double getA() {
    return a;
  }

  public double getB() {
    return b;
  }

  public double getC() {
    return c;
  }
}
